package com.advanced.dome.spring.all;

/**
 * @author guohong
 * 储存bean中的ref属性
 */
public class BeanReference {
    private String name;

    private String ref;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRef() {
        return ref;
    }

    public void setRef(String ref) {
        this.ref = ref;
    }
}
